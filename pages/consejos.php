                <p>Para un correcto embalaje debes utilizar una caja adecuada y seguir una serie de pasos:</p>
                <ul>
                    <li>Las cajas deben ser de un material rígido.</li>
                    <li>Asegúrate de que el embalaje no está dañado.</li>
                    <li>Si hay más de un objeto en la caja, envuelve cada objeto por separado en papel de burbuja, así evitarás que puedan dañarse entre ellos.</li>
                    <li>Introduce en el fondo de la caja una capa de material acolchado de una profundidad mínima de 5 cm. Los artículos más delicados y pesados necesitarán más acolchado en el fondo, laterales y parte superior de la caja.</li>
                    <li>Cierra la caja con cinta adhesiva de al menos 50 mm de ancho. Ya tendrías tu paquete bien embalado pero como últimos detalles te aconsejamos que no utilices cadenas o cables ya que pueden enredarse o engancharse.</li>
                    <li>Coloca las etiquetas y direcciones de envío en la parte superior de la caja, eso sí, evitando ponerlos en los cierres, esquinas o cintas adhesivas.</li>
                </ul>
